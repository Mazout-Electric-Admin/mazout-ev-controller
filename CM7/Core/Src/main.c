/**
 * @brief Timer period elapsed callback for BLDC motor control.
 *
 * This callback is triggered when a timer (here, TIM4) reaches its period.
 * It implements a six-step commutation sequence for a sensorless BLDC motor by
 * selecting a commutation step and adjusting the PWM duty cycle based on the RPM preset.
 *
 * The RPM preset (rpm_preset_t) determines how the repetition counter (repeat)
 * and the PWM duty cycle (_duty) are adjusted:
 *   - Preset 1: Gradually reduce the repeat counter until it reaches 4,
 *               increasing the duty cycle by 10 for each decrement.
 *   - Preset 2: Similar to preset 1, but the repeat counter is reduced until it
 *               reaches 2.
 *   - Preset 3: Immediately set the repeat counter to 0 and increment the duty cycle.
 *
 * After updating the PWM parameters, the function calls SetCommutationStep() to
 * update the PWM outputs for the current commutation step, cycles to the next step,
 * and then updates the timer registers (auto-reload and repetition counter) for the next cycle.
 *
 * @param htim Pointer to the timer handle structure.
 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  // Verify that the interrupt is from Timer 4
  if (htim->Instance == TIM4)
  {
    // Adjust PWM duty and repetition counter based on the selected RPM preset
    switch (rpm_preset_t) // RPM Preset selection
    {
    case 1:
      // For Preset 1: Decrement 'repeat' until it reaches 4, increasing _duty by 10 on each decrement.
      while (repeat > 4)
      {
        repeat--;
        _duty += 10;
      }
      break;

    case 2:
      // For Preset 2: Decrement 'repeat' until it reaches 2, increasing _duty by 10 on each decrement.
      while (repeat > 2)
      {
        repeat--;
        _duty += 10;
      }
      break;

    case 3:
      // For Preset 3: Immediately reset 'repeat' to 0 and increase _duty by 10.
      repeat = 0;
      _duty += 10;
      break;
    }

    // Update PWM outputs based on the current commutation step.
    // This function handles the switching of active/inactive PWM channels.
    SetCommutationStep(step, _duty);

    // Cycle through the 6-step commutation sequence for the BLDC motor.
    if (++step >= 6)
    {
      step = 0; // Reset step count after completing one full cycle.
    }

    // Update the timer's auto-reload value and repetition counter for the next PWM cycle.
    __HAL_TIM_SET_AUTORELOAD(&htim1, period);
    htim1.Instance->RCR = repeat;
  }
}

/**
 * @brief Configures PWM channels for a specific BLDC motor commutation step.
 *
 * This function sets the PWM duty cycle and starts or stops PWM outputs on Timer 1
 * based on the current commutation step (0-5). Each step corresponds to a specific
 * switching pattern across three phases (channels), which is essential for driving
 * a BLDC motor in a six-step commutation scheme.
 *
 * For each commutation step:
 *   - One phase is driven with the PWM signal at the specified duty cycle.
 *   - Another phase is either pulled low (set to 0) or disabled.
 *   - The third phase is turned off to ensure the correct conduction path.
 *
 * Note: The mapping between channels and motor phases should be verified
 * according to the hardware design and motor driver configuration.
 *
 * @param step The current commutation step (0 to 5).
 * @param duty The PWM duty cycle to be applied.
 */
void SetCommutationStep(uint8_t step, uint16_t duty)
{
  switch (step)
  {
  case 0:
    // Commutation Step 0:
    // - Phase A (TIM_CHANNEL_1): PWM active with given duty.
    // - Phase B (TIM_CHANNEL_2): Set low (compare = 0) but PWM clock is running.
    // - Phase C (TIM_CHANNEL_3): PWM outputs stopped.
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);

    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_3);
    break;

  case 1:
    // Commutation Step 1:
    // - Phase A (TIM_CHANNEL_1): PWM active with given duty.
    // - Phase B (TIM_CHANNEL_2): PWM outputs stopped.
    // - Phase C (TIM_CHANNEL_3): Set low (compare = 0) and activated.
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);

    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_2);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
    break;

  case 3:
    // Commutation Step 3:
    // - Phase A (TIM_CHANNEL_1): Set low (compare = 0) and activated.
    // - Phase B (TIM_CHANNEL_2): PWM active with given duty.
    // - Phase C (TIM_CHANNEL_3): PWM outputs stopped.
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);

    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_3);
    break;

  case 2:
    // Commutation Step 2:
    // - Phase A (TIM_CHANNEL_1): PWM outputs stopped.
    // - Phase B (TIM_CHANNEL_2): PWM active with given duty.
    // - Phase C (TIM_CHANNEL_3): Set low (compare = 0) and activated.
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_1);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
    break;

  case 4:
    // Commutation Step 4:
    // - Phase A (TIM_CHANNEL_1): Set low (compare = 0) and activated.
    // - Phase B (TIM_CHANNEL_2): PWM outputs stopped.
    // - Phase C (TIM_CHANNEL_3): PWM active with given duty.
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_1);

    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_2);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
    break;

  case 5:
    // Commutation Step 5:
    // - Phase A (TIM_CHANNEL_1): PWM outputs stopped.
    // - Phase B (TIM_CHANNEL_2): Set low (compare = 0) and activated.
    // - Phase C (TIM_CHANNEL_3): PWM active with given duty.
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_1);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 0);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);

    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, duty);
    HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_3);
    break;

  default:
    // Default case: Safety fallback to stop all PWM outputs.
    // This ensures that if an invalid commutation step is provided,
    // all channels are disabled to prevent unintended motor behavior.
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_1);
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_2);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_2);
    HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_3);
    HAL_TIMEx_PWMN_Stop(&htim1, TIM_CHANNEL_3);
    break;
  }
}

